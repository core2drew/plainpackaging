            <div class="content-desc-cont col-lg-7 col-lg-offset-1">
                <p>Since the states settled their lawsuits against the major tobacco companies in 1998, our annual reports have assessed whether the states are keeping their promise to use a significant portion of their settlement funds – estimated at $246 billion over the first 25 years – to attack the enormous public health problems caused by tobacco use in the United States.</p>
                <p>Despite the huge sums they get from the tobacco settlement and billions more they collect in tobacco taxes, the states continue to shortchange tobacco prevention and cessation programs that are proven to save lives and money.</p>
                <p>In the current budget year, Fiscal Year 2016, the states will collect $25.8 billion from the settlement and taxes. But they will spend only 1.8 percent of it – $468 million – on programs to prevent kids from smoking and help smokers quit. (As of December 8, 2015, when this report was published, two states – Illinois and Pennsylvania – had yet to enact 2016 budgets, so they have not set funding levels for tobacco prevention programs.)</p>
                <p>Meanwhile, tobacco companies spend $9.6 billion a year – more than one million dollars each hour – to market their deadly and addictive products, according to the most recent data from the Federal Trade Commission.  This means tobacco companies spend $20 to market their products for every $1 the states spend to reduce tobacco use.</p>
                <p>This giant gap is undermining efforts to save lives and health care dollars by reducing tobacco use, the number one cause of preventable death in the United States.</p>
                <p>This report is issued by the Campaign for Tobacco-Free Kids, American Heart Association, American Cancer Society Cancer Action Network, American Lung Association, Robert Wood Johnson Foundation, Americans for Nonsmokers’ Rights and Truth Initiative.</p>
